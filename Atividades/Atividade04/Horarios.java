public class Horarios 
{
    //Atributos
    private String dia;
    private String hora;

    //Construtor
    public Horarios()
    {

    }

    //Método de acesso do dia
    public String getDia()
    {
        return dia;
    }

    //Método de edição do dia
    public void setDia(String d)
    {
        dia = d;
    }

    //Método de acesso da hora
     public String getHora()
     {
        return hora;
     }

     //Método de edição da hora
     public void setHora(String h)
     {
        hora = h;
     }
}
